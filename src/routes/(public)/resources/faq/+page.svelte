      <h2>What is Scouting All About?</h2>
      <p>Scouting America is one of the largest youth organizations in the United States, with about 1.2 million youth participants. The BSA was founded in 1910, and since then about 110 million Americans have participated in BSA programs. The BSA is part of the international Scout movement and became a founding member organization of the World Organization of the Scout Movement in 1922.
        The stated mission of Scouting America is to "prepare young people to make ethical and moral choices over their lifetimes by instilling in them the values of the Scout Oath and Law." Youth are trained in responsible citizenship, character development, and self-reliance through participation in a wide range of outdoor activities, educational programs, and, at older age levels, career-oriented programs in partnership with community organizations.
      </p>
      <p>Per the Scout Law: <strong>a Scout is trustworthy, loyal, helpful, friendly, courteous, kind, obedient, cheerful, thrifty, brave, clean, and reverent.</strong>
      </p>
      <h2>Troop 185, Wellesley</h2>
      <p>Troop 185 is a Scouts BSA Troop affiliated with Scouting America and charted by the St. John’s / St. Paul’s Parish Collaborative in Wellesley, MA. The Troop has been active for over 50 years.</p>
      <ul>
      <li>There are approximately 45 active scouts and 8 active adult leaders in the Troop.</li>
      <li>Last year we had 8 scouts earn the rank of Eagle Scout.</li>
      <li>We meet weekly on Monday at 7:30 PM at St. Paul’s Church, excluding school holidays. The parking entrance is off Atwood Street.</li>
      <li>Parents are welcomed to attend meetings, but they are not required to. Most parents drop off their scouts for the meeting.</li>
      <li>We have an optional trip about every month. A few examples of recent trips:
        <ul>
        <li>Canoe adventure down the Saco River</li>
        <li>Bike trip at Camp Richard on Nantucket</li>
        <li>Mt. Monadnock camping and climbing</li>
        <li>Freeze Out - Nobscot overnight in February</li>
        <li>Winter Carnival Ski Weekend in Vermont</li>
        <li>Backpacking trip to an AMC hut in the White Mountains</li>
        <li>Fishing Derby at Camp Resolute</li>
        <li>Wellesley Veterens Day Parade</li>
        <li>MetroRock Indoor Climbing Gym Overnight</li>
        <li>Mt Israel, and Mt Cardigan Hikes</li>
        </ul>
      </li>
      <li>We attend Scout summer camp at Camp Yawgoog in Rhode Island each summer for one week in early August.</li>
      <li>High School aged Scouts join Annual "High Adventure" summer excursions to Philmont, Maine, Alaska, Yellowstone, or Florida Sea Base.</li>
      <li>Senior Scouts may be voted into Scouting America's Honor Society: the Order of the Arrow</li>
      <li>The Troop is organized into patrols, roughly by age group. Each patrol elects a leader. The Troop is led by two Senior Patrol Leaders.</li>
      <li>Troop meetings/events are scout led. Patrol Leaders / Senior Patrol Leaders form the Patrol Leadership Council (PLC), have significant responsibility to plan and facilitate meetings. Adults play an essential role as advisors: overseeing safety, providing support, sharing knowledge, and providing administrative/financial support. This system may seem chaotic at times, but it provides invaluable youth leadership development opportunities. The system works!</li>
      <li>Troop meetings last 90 minutes and generally consist of these parts:
        <ul>
          <li>Opening, flag ceremony.</li>
          <li>Scout skills training.</li>
          <li>Advancement, merit badge work.</li>
          <li>Games / activities.</li>
          <li>Closing, Scoutmaster Minute.</li>
        </ul>
        </li>
        <li>Advancement is Based on Experiential Learning: Exciting and meaningful activities are offered - and education happens. Learning comes from doing. For example, youth may read about first aid, hear it discussed, and watch others administer it, but they will not learn it until they practice it. Rushing a Scout through requirements to obtain a badge is not the goal. Advancement should be individually based and a natural outcome. Scouts will grow, succeed and learn to advocate for themselves.</li>
        <li>Scouting Ranks
          <ul><li>Scout</li>
            <li>Tenderfoot</li>
          <li>Second Class</li>
          <li>First Class</li>
          <li>Star</li>
          <li>Life</li>
          <li>Eagle</li>
          </ul></li>
        <li>Merit Badges: Scouts can learn about sports, crafts, science, trades, business, and future careers as they earn merit badges.There are more than 135 merit badge and many of the Eagle Required are offered during our Troop meetings. There is no time limit for starting and completing a merit badge, but all work must be completed by age 18.</li>
       
      <li>Webelos typically join during our February Court of Honor, but we welcome eligible youth at any time. Arrow of Light is not a prerequisite. Eligibility requirements: Youth can join Troop 185 if they have completed the fifth grade and are at least 10 years old, OR have earned the Arrow of Light Award and are at least 10 years old, OR are age 11 but have not reached age 18.</li>
      </ul>
